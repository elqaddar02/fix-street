    <div class="bg-white rounded-xl shadow-sm p-6">
        <h2 class="text-2xl font-bold text-gray-900 mb-4">Qu’est-ce que Madinova ?</h2>
        <div class="space-y-4 text-gray-700">
            <p>
                Madinova est une plateforme citoyenne indépendante qui permet aux habitants de signaler des problèmes dans leurs rues et quartiers,
                comme les nids-de-poule, les lampadaires cassés, les déchets, les graffitis ou d’autres problèmes publics.
            </p>
            <p>
                Madinova n’est pas un site gouvernemental. Cependant, les signalements envoyés sont transmis aux autorités compétentes
                afin qu’elles puissent intervenir rapidement.
            </p>
            <p>
                Tous les signalements sont également publiés sur la plateforme afin que les autres citoyens puissent voir les problèmes déjà signalés,
                éviter les doublons et suivre l’évolution de leur quartier.
            </p>
        </div>
    </div>

    <div class="bg-white rounded-xl shadow-sm p-6">
        <h2 class="text-2xl font-bold text-gray-900 mb-4">Comment faire un signalement ?</h2>
        <div class="space-y-4 text-gray-700">
            <p>Faire un signalement sur Madinova est simple et rapide :</p>
            <ul class="list-disc pl-6 space-y-2">
                <li>Allez sur la page d’accueil et choisissez votre localisation sur la carte ou recherchez votre zone.</li>
                <li>Placez un point exactement à l’endroit du problème.</li>
                <li>Décrivez clairement le problème (ex : nid-de-poule, éclairage cassé, déchets, etc.).</li>
                <li>Ajoutez des photos si possible pour mieux illustrer la situation.</li>
                <li>Envoyez votre signalement et attendez la confirmation.</li>
            </ul>
            <p>
                Après l’envoi, votre signalement sera vérifié et transmis aux services concernés pour traitement.
            </p>
        </div>
    </div>

